package com.ink.unforgiving.command;

/**
 * All arguments of the toggle argument of the <code>CommandKOS</code> command.
 *
 * <p> This class contains the following elements:
 *     <ul>
 *         <li><code>RENDER_TRUCES</code>  - Toggle rendering of truces list.</li>
 *         <li><code>RENDER_FRIENDS</code> - Toggle rendering of friends list.</li>
 *         <li><code>RENDER_ENEMIES</code> - Toggle rendering of enemies list.</li>
 *     </ul>
 * </p>
 *
 * @see CommandKOS
 */
public enum CommandKOSToggles {
    RENDER_TRUCES,
    RENDER_FRIENDS,
    RENDER_ENEMIES,
}
